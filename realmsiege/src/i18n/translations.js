export const setLang = (l) => localStorage.setItem('rs_lang', l);
export const t = (path) => {
  const lang = getLang();
  const keys = path.split('.');
  let obj = translations[lang];
  for (const k of keys) { obj = obj?.[k]; }
  return obj || path;
};
